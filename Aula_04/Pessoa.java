package Atividade;

public class Pessoa {
	
	// Atributos
	
	private String nome, endereco;
	private int idade;
	
	// Métodos NOME
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	// Métodos IDADE
	
	public int getIdade() {
		return idade;
	}
	
	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	// Métodos ENDEREÇO
	
	public String getEndereco() {
		return endereco;
	}
	
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}	
	
}
