package Atividade;

public class Carro {
	
	// Atributos
	
	private String nome;
	private int potencia;
	
	// Métodos NOME
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	// Métodos POTENCIA
	
	public int getPotencia() {
		return potencia;
	}

	public void setPotencia(int potencia) {
		this.potencia = potencia;
	}
}
